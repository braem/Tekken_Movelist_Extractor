package extractor;

public class Char
{
	String id;
	String name;
	String fullname;
	String sex;
	String origin;
	String fightingStyle;
	
	public Char(String id, String name, String fullname, String sex, String origin, String fightingStyle) {
		this.id = id;
		this.name = name;
		this.fullname = fullname;
		this.sex = sex;
		this.origin = origin;
		this.fightingStyle = fightingStyle;
	}
}
